//To print the Grade System using if else

// Declared variable as mark and assigned a value to it
let mark = 55

//Using if else condition writing logic to print it's grade
if (mark>85){
    console.log("Grade is : A")
}
else if(mark>60){
    console.log("Grade is : B")
}
else if(mark>50){
    console.log("Grade is : C")
}
else{
    console.log("Grade is :D")
}